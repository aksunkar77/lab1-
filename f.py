#python casting 
#in this file i will work with float integer and string then i will transform them
#float
a = float(1)     # x will be 1.0
b = float(2.8)   # y will be 2.8
c = float("3")   # z will be 3.0
d = float("4.2") # w will be 4.2
print(a, b, c, d)
print(a+b+c+d)

#integer
e = int(1)   # x will be 1
f = int(2.8) # y will be 2
g = int("3") # z will be 3
print(e, f, g)
print(e+f+g)

#string
h = str("s1") # x will be 's1'
i = str(2)    # y will be '2'
j = str(3.0)  # z will be '3.0' 
print(h, i, j)
print(h+i+j)
